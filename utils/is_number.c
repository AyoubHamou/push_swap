#include "push_swap.h"

void	is_number(char *str)
{
	int	i;

	i = 0;
	if (!str[i])
		error_message();
	if ((str[i] == '+' || str[i] == '-') && str[i+1] != '\0')
		i++;
	while(str[i])
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			error_message();
		i++;
	}
}
